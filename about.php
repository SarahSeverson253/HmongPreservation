<h1>The Hmong Preservation Project</h1>

<h2>Our Story</h2>

<p>The Hmong Preservation Project was created by Dr. Cheng Thao, Rhodia Xiong, and
Sarah Severson of the Computer Science department at Concordia University, St. Paul.</p>

<p>Because of the large Hmong population in the Twin Cities area of Minnesota,
Concordia University, St. Paul is lucky to host many Hmong students and even its own Hmong 
Studies department, which helps preserve Hmong traditions, stories, and history through classes
offered on campus and hosts an annual event called the International Conference on 
Hmong Studies.</p>

<h2>Our Goal</h2>

<p>As time passes, many of the documents and photos of the original generation of Hmong peoples
who came over to the US are unfortunately being lost, are fading, or just are stored away and
kept from the public light.</p>

<p>Because there aren't many large archives for the history of the Hmong peoples yet, Concordia University,
St. Paul wishes to remedy this by building and hosting one! Together with your help, we can create
a sophisticated archive that people across the world can visit and enjoy not only for research purposes,
but also to gain a greater understanding of the Hmong peoples.

<h2>How You Can Help</h2>

<p>The most important thing you can do to help us is to submit your stories and images! This archive
will only thrive if the Hmong community comes forward and generously donates their photos and stories to it.
Through doing this, you will preserve your family's history - and the history of all the Hmong peoples -
for generations to come.</p>

